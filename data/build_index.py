INDEX = REPO / "index.html"


def extract_recent_updates(src: str) -> str:
    m = re.search(r'<details class="recent-updates">.*?</details>', src, re.DOTALL)
    return m.group(0) if m else (
        '<details class="recent-updates"><summary>&#9670; Recent Updates</summary>'
        '<ul></ul></details>'
    )


