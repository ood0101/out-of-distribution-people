"""
build_index.py
==============
(Re)build index.html as the interactive DIRECTORY TABLE.

The table renders client-side from data/directory.json (regenerated by
build_directory.py from the live outreach_state.json), so status / tier /
last-contacted are always current. This script just (re)writes the static
shell: header, controls, table markup, CSS, and the render/sort/filter JS.

It preserves the existing "Recent Updates" changelog block by extracting it
from the current index.html, so re-running is safe.

Usage:
    python3 data/build_directory.py     # refresh the data
    python3 data/build_index.py         # rebuild the shell (rarely needed)
"""
